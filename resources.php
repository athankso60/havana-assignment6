			<h1 class="heading1">Citation Resources</h1>

			<p>Citing is important to cite sources you used in your research for several reasons: To show your reader you've done proper research by listing sources you used to get your information. To be a responsible scholar by giving credit to other researchers and acknowledging their ideas. Citation is also important in order to abide by the rules of academic honesty. Please locate the academic honesty rules to know exactly what the teacher expects of you. If any of these rules are broken, whether or not you broke a rule knowingly, you will face the burdening consequences. </p> 

